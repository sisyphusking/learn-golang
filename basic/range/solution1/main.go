package main

import "fmt"

func main() {

	s := "你好"
	//对s进行遍历，输出每个字符
	for i, c := range s {
		fmt.Printf("%d %b %s\n", i, c, string(c))
	}

	fmt.Printf("\n")
	//对s进行遍历，输出每个字字
	for i := 0; i < len(s); i++ {
		fmt.Printf("%d %b\n", i, s[i])
	}
}

